short daox_tt_short( uchar_t *data )
{
	return (data[0]<<8) + data[1];
}
ushort_t daox_tt_ushort( uchar_t *data )
{
	return (data[0]<<8) + data[1];
}
long daox_tt_long( uchar_t *data )
{
	return (data[0]<<24) + (data[1]<<16) + (data[2]<<8) + data[3];
}
